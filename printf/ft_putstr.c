#include "ft_printf.h"

int	ft_putstr(char *c)
{
	int	i;

	i = 0;
	if (!c)
	{
		write(1, "(null)", 6);
		i += 6;
	}
	else
	{
		while (c[i])
		{
			ft_putchar(c[i]);
			i++;
		}
	}
	return (i);
}
